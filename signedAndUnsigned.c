////
//// Signed and Unsigned.
//// Variables such as int and char can be classified into signed and unsigned
////For int unsigned can only take positive values  with range 0-2^32
////Signed int can take negative and positive integers with range -2^31 to 2^31
////Unsigned char range between 0-256
////Signed char range between -127-127
//Example-
#include<stdio.h>

int main(){
    unsigned int result;
    result = 9-90;
    printf("Result = %d", result);
    return 0;
}


